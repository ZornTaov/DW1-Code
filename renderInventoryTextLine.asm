int renderInventoryTextLine(lineId) {
  for(i = 0; i < 2; i++) {
    itemId = load(0x13D474 + lineId + i)
    
    if(itemId == -1)
      continue
    
    store(0x13D4B0 + lineId + i, lineId + i)
    
    xPos = ((lineId + i) % 2) * 96
    yPos = ((lineId + i) / 2) * 12
    renderString(0x1269DC + itemId * 0x20, xPos, yPos)
  }
  
  return drawSync(0)
}

