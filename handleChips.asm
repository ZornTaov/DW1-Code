void handleChips(int itemId) {
  
  offense = defense = speed = brains = hp = mp = lifetime = 0
    
  /*
   * 0x800C454C
   * 0x800C457C
   * 0x800C458C
   * 0x800C459C
   * 0x800C45AC
   * 0x800C45BC
   * 0x800C45CC
   * 0x800C45DC
   * 0x800C4600
   * 0x800C4624
   * 0x800C4648
   */
  switch(itemId - 0x16) {
    case 0: // autopilot
      if(load(0x134FF4) == 1) { // is not interacting/loading something?
        0x000C55FC()
        callScriptSection(0, 0x4DD, 0)
      }
      break
    case 1:
      offense = 50
      break
    case 2:
      defense = 50
      break
    case 3:
      brain = 50
      break
    case 4:
      speed = 50
      break
    case 5:
      hp = 500
      break
    case 6:
      mp = 500
      break
    case 7:
      offense = 100
      brain = 100
      lifetime = -24
      break
    case 8:
      defense = 100
      speed = 100
      lifetime = -24
      break
    case 9:
      hp = 1000
      mp = 1000
      lifetime = -24
      break
    case 10: // porta potty
      handlePortaPotty()
      return
  }
  
  healValue(0x1557F0, hp, 9999)
  healValue(0x1557F2, mp, 9999)
  healValue(0x1557E0, offense, 999)
  healValue(0x1557E2, defense, 999)
  healValue(0x1557E4, speed, 999)
  healValue(0x1557E6, brain, 999)
  modifyLifetime(lifetime)
  
  if(itemId > 0x1D && itemId < 0x20) // check is DV Chip
    addTamerLevel(10, -1)
}

