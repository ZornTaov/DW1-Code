int fireFlattenProjectile(entityPtr) {
  dataStartPtr = load(0x1350E0)
  originalType = getOriginalType(load(entityPtr))
  
  for(id = 0; id < 8; id++)
    if(load(dataStartPtr + id * 0x24) < 0)
      break
  
  if(id == 8)
    return -1
  
  dataPtr = dataStartPtr + id * 0x24
  modelPtr = load(entityPtr + 0x04)
  locPtr = modelPtr + 0x34
  height = load(0x12CECE + load(entityPtr) * 52) // height
  
  store(dataPtr + 0x00, 0)
  store(dataPtr + 0x1C, 0x7383C + originalType * 12)
  store(dataPtr + 0x20, entityPtr)
  
  store(dataPtr + 0x04, 0)
  store(dataPtr + 0x06, -height * 50 / 100)
  store(dataPtr + 0x08, -height * 50 / 100)
  
  rotateVector(locPtr, dataPtr + 0x04, dataPtr + 0x04)
  
  store(dataPtr + 0x04, load(dataPtr + 0x04) + load(locPtr + 0x14))
  store(dataPtr + 0x06, load(dataPtr + 0x06) + load(locPtr + 0x18))
  store(dataPtr + 0x08, load(dataPtr + 0x08) + load(locPtr + 0x1C))
  store(dataPtr + 0x0C, 0)
  store(dataPtr + 0x0E, load(modelPtr + 0x72))
  store(dataPtr + 0x10, 0)
  store(dataPtr + 0x14, 0)
  store(dataPtr + 0x16, 0)
  store(dataPtr + 0x18, 0xFFDD)
  
  rotateVector(locPtr, dataPtr + 0x14, dataPtr + 0x14)
  
  setObject(0x179, id, 0x70C7C, 0x70E14)
  
  return id
}

