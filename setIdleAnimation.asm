void setIdleAnimation() {
  plannedAnimation = load(0x134E2C)
  
  // is idle and Digimon is close to tamer?
  if(plannedAnimation == 1 || plannedAnimation == 0 && load(0x134E24) == 2)
    return
  
  // sleepy, tired, injured or sick
  if(load(0x138460) & 0x63 != 0)
    store(0x134E2C, 1)
  else
    store(0x134E2C, 0)
  
  startAnimation(0x1557A8, load(0x134E2C))
}

