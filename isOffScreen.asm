  xPos = screenPos[0] - (160 - load(0x134EC0))
  yPos = screenPos[1] - (120 - load(0x134EC4))
  
  if(xPos < -width / 2)
    return 1
  
  if(xPos > width / 2)
    return 1
  
  if(yPos < -height / 2)
    return 1
  
  if(yPos > height / 2)
    return 1
  
  return 0
}

